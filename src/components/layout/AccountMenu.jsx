import * as React from 'react';
import Box from '@mui/material/Box';
import Avatar from '@mui/material/Avatar';
import Menu from '@mui/material/Menu';
import MenuItem from '@mui/material/MenuItem';
import ListItemIcon from '@mui/material/ListItemIcon';
import Divider from '@mui/material/Divider';
import IconButton from '@mui/material/IconButton';
import Typography from '@mui/material/Typography';
import Tooltip from '@mui/material/Tooltip';
import PersonAdd from '@mui/icons-material/PersonAdd';
import Settings from '@mui/icons-material/Settings';
import Logout from '@mui/icons-material/Logout';
import styled from '@emotion/styled';
import { colors } from '../../assets/MUI/Colors';
import { useNavigate } from 'react-router-dom';
import { useContext } from 'react';
import { UserContext } from '../../_auth/context/UserContext';

export default function AccountMenu({ setIsLogin }) {
    const navigate = useNavigate(); //Inicio el historial

    const AvatarStyle = styled(Avatar)`
        .MuiBox-root, .MuiButtonBase-root, .MuiAvatar-root{
            display: flex;
            align-items: center;
            justify-content: center;
            position: relative;
            left: 0;
            background-color: ${colors.buttonColor}
        }
    `

    const { usuario, setUsuario } = useContext(UserContext);

    const [anchorEl, setAnchorEl] = React.useState(null);
    const open = Boolean(anchorEl);
    const handleClick = (event) => {
        setAnchorEl(event.currentTarget);
    };
    const handleClose = () => {
        setAnchorEl(null);
    };

    const handlePorfile = () => {
        navigate('/profile');
    };

    const handleLogOut = (e) => {
        e.preventDefault();
        navigate('/sign-in');
        setIsLogin([]);
        setUsuario({});
    }
    return (
        <React.Fragment>
            <Box sx={{ display: 'flex', alignItems: 'center', textAlign: 'center' }}>
                <Tooltip title="Account settings">
                    <IconButton
                        onClick={handleClick}
                        size="small"
                        sx={{ ml: 0 }}
                        aria-controls={open ? 'account-menu' : undefined}
                        aria-haspopup="true"
                        aria-expanded={open ? 'true' : undefined}
                    >
                        <Avatar alt={usuario.name} src="src\assets\img\About-Us\DanielIñiguezz.jpeg" sx={{ width: 32, height: 32, bgcolor: colors.buttonColor }}></Avatar>
                    </IconButton>
                </Tooltip>
            </Box>
            <Menu
                anchorEl={anchorEl}
                id="account-menu"
                open={open}
                onClose={handleClose}
                onClick={handleClose}
                PaperProps={{
                    elevation: 0,
                    sx: {
                        overflow: 'visible',
                        filter: 'drop-shadow(0px 2px 8px rgba(0,0,0,0.32))',
                        mt: 1.5,
                        '& .MuiAvatar-root': {
                            width: 32,
                            height: 32,
                            ml: -0.5,
                            mr: 1,
                        },
                        '&::before': {
                            content: '""',
                            display: 'block',
                            position: 'absolute',
                            top: 0,
                            right: 14,
                            width: 10,
                            height: 10,
                            bgcolor: 'background.paper',
                            transform: 'translateY(-50%) rotate(45deg)',
                            zIndex: 0,
                        },
                    },
                }}
                transformOrigin={{ horizontal: 'right', vertical: 'top' }}
                anchorOrigin={{ horizontal: 'right', vertical: 'bottom' }}
            >
                <MenuItem onClick={handlePorfile}>
                    <Avatar alt={usuario.name} src="src\assets\img\About-Us\DanielIñiguezz.jpeg" sx={{ width: 32, height: 32, bgcolor: colors.buttonColor }} />{usuario.firstName} {usuario.lastName}
                </MenuItem>
                <Divider />
                <MenuItem onClick={handleClose}>
                    <ListItemIcon>
                        <Settings fontSize="small" />
                    </ListItemIcon>
                    Configuracion
                </MenuItem>
                <MenuItem onClick={handleLogOut}>
                    <ListItemIcon>
                        <Logout fontSize="small" />
                    </ListItemIcon>
                    Cerrar la sesion de @{usuario.userName}
                </MenuItem>
            </Menu>
        </React.Fragment>
    );
}